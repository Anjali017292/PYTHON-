import numpy as np
#creating ones array
array1=np.ones((3,4))
print("Ones array of order (3,4) :")
print(array1)
print("------------------")
#Ones array
array2=np.ones((5,3),dtype=int)
print("Ones array of order (5,3) :")
print(array2)
print("------------------")
#Ones array
array3=np.ones((4,3),dtype=int)
print("Ones array of order (4,3) :")
print(array3)
print("------------------")

