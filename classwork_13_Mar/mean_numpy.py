import numpy as np
#array
array1=np.array([32,56,78,90,56,45,89,12])
print("First array :")
print(array1)
print("---------------")
#mean
print("Mean is:")
print(np.mean(array1))
print("---------------")
#standard deviation
print("Standard deviation is:")
print(np.std(array1))
print("---------------")

