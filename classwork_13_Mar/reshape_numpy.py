import numpy as np
#array
array1=np.array([32,56,78,90,56,45,89,12])
print("First array :")
print(array1)
print("---------------")
#reshaping in order (4,2)
print(array1.reshape(2,1,4))
print("---------------")
print(array1.reshape(4,1,2))
print("---------------")
print(array1.reshape(1,2,4))
print("---------------")
print(array1.reshape(2,4,1))
print("---------------")
print(array1.reshape(1,4,2))
print("---------------")
print(array1.reshape(4,2,1))
print("---------------")
print(array1.reshape(2,2,2))
print("---------------")

