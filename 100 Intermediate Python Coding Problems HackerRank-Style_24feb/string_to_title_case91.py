# Convert a string to title case
original_string = "hello world"
title_case_string = original_string.title()

# The title() method converts the first character of each word to uppercase and the rest to lowercase, effectively converting the string to title case. Finally, the code prints the original string and the title case string.
print(f"Original string: {original_string}")
print(f"Title case string: {title_case_string}")

'''
Output:
Original string: hello world
Title case string: Hello World
'''